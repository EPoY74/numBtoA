import pytest

from src.main import main


@pytest.mark.parametrize(
    "a,b,expected",
    [
        ("8350", "0009", "9350"),  # заменили 8 -> 9
        ("1234", "99", "9934"),  # две замены, пока есть цифры B
        ("1234", "", "1234"),  # B пустая
        ("-99", "0", "-09"),  # делаем ближе к 0: 9 -> 0 в старшем разряде
        ("-10", "0", "00"),  # -00 нормализуется в "0"
        ("0", "987", "9"),  # 0 можно улучшить на 9
        ("-1", "", "-1"),  # без B остаётся как есть
    ],
)
def test_main(a, b, expected):
    assert main(a, b) == expected
